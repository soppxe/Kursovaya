package model;

/**
 * Модель данных для представления пользователя системы "Калькулятор металлурга".
 * Содержит информацию об учетной записи пользователя и его учетных данных.
 *
 * <p><b>Основное назначение:</b>
 * <ul>
 * <li>Хранение учетных данных пользователя для аутентификации</li>
 * <li>Идентификация пользователя в системе</li>
 * <li>Связь пользователя с выполняемыми расчетами</li>
 * <li>Управление правами доступа и персональными настройками</li>
 * </ul>
 *
 * <p><b>Безопасность данных:</b>
 * <ul>
 * <li>Пароль хранится в зашифрованном виде</li>
 * <li>Идентификатор обеспечивает уникальность записей</li>
 * <li>Email используется для восстановления доступа и уведомлений</li>
 * </ul>
 *
 * @author Саитова София
 * @version 1.0
 * @since 2025
 */
public class User {

    /**
     * Уникальный идентификатор пользователя в базе данных.
     * Используется для однозначной идентификации пользователя и связей с другими сущностями.
     * Генерируется автоматически при создании новой записи.
     */
    private int id;

    /**
     * Уникальное имя пользователя для входа в систему.
     * Должно быть уникальным в пределах системы.
     * Используется для аутентификации и отображения в интерфейсе.
     */
    private String username;

    /**
     * Зашифрованный пароль пользователя.
     * Хранится в виде хеша для обеспечения безопасности.
     * Никогда не сохраняется в открытом виде.
     */
    private String encryptedPassword;

    /**
     * Электронная почта пользователя.
     * Используется для уведомлений, восстановления пароля и связи с пользователем.
     * Может быть null для некоторых типов учетных записей.
     */
    private String email;

    /**
     * Конструктор по умолчанию.
     * Создает пустой экземпляр пользователя для последующего заполнения полей.
     * Используется фреймворками ORM и при десериализации данных.
     */
    public User() {}

    /**
     * Конструктор для создания пользователя с основными данными.
     * Используется при регистрации нового пользователя в системе.
     *
     * @param username уникальное имя пользователя, не должно быть null
     * @param encryptedPassword зашифрованный пароль, не должно быть null
     * @param email электронная почта пользователя, может быть null
     */
    public User(String username, String encryptedPassword, String email) {
        this.username = username;
        this.encryptedPassword = encryptedPassword;
        this.email = email;
    }

    // ГЕТТЕРЫ И СЕТТЕРЫ

    /**
     * Возвращает уникальный идентификатор пользователя.
     *
     * @return идентификатор пользователя
     */
    public int getId() { return id; }

    /**
     * Устанавливает уникальный идентификатор пользователя.
     *
     * @param id идентификатор пользователя
     */
    public void setId(int id) { this.id = id; }

    /**
     * Возвращает имя пользователя для входа в систему.
     *
     * @return имя пользователя
     */
    public String getUsername() { return username; }

    /**
     * Устанавливает имя пользователя для входа в систему.
     *
     * @param username имя пользователя
     */
    public void setUsername(String username) { this.username = username; }

    /**
     * Возвращает зашифрованный пароль пользователя.
     *
     * @return зашифрованный пароль
     */
    public String getEncryptedPassword() { return encryptedPassword; }

    /**
     * Устанавливает зашифрованный пароль пользователя.
     *
     * @param encryptedPassword зашифрованный пароль
     */
    public void setEncryptedPassword(String encryptedPassword) { this.encryptedPassword = encryptedPassword; }

    /**
     * Возвращает электронную почту пользователя.
     *
     * @return электронная почта пользователя
     */
    public String getEmail() { return email; }

    /**
     * Устанавливает электронную почту пользователя.
     *
     * @param email электронная почта пользователя
     */
    public void setEmail(String email) { this.email = email; }
}