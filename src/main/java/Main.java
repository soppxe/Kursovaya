import auth.LoginFrame;
import service.DatabaseService;

import javax.swing.*;

/**
 * Главный класс приложения "Калькулятор металлурга".
 * Служит точкой входа в программу и отвечает за инициализацию основных компонентов системы.
 *
 * <p><b>Основные функции:</b>
 * <ul>
 * <li>Инициализация базы данных при запуске приложения</li>
 * <li>Запуск графического пользовательского интерфейса</li>
 * <li>Обеспечение корректного запуска в потоке обработки событий Swing</li>
 * </ul>
 * </p>
 *
 * <p><b>Архитектура запуска:</b>
 * <ol>
 * <li>Инициализация службы работы с базой данных</li>
 * <li>Создание и отображение окна аутентификации</li>
 * <li>Передача управления главному циклу обработки событий Swing</li>
 * </ol>
 * </p>
 *
 * <p><b>Используемые компоненты:</b>
 * <ul>
 * <li>{@link DatabaseService} - служба для работы с базой данных</li>
 * <li>{@link LoginFrame} - основное окно аутентификации пользователя</li>
 * <li>{@link SwingUtilities} - утилиты для работы с Swing в потокобезопасном режиме</li>
 * </ul>
 * </p>
 *
 * @author Саитова София
 * @version 1.0
 * @see DatabaseService
 * @see LoginFrame
 * @see SwingUtilities
 * @since 2025
 */
public class Main {

    /**
     * Точка входа в приложение "Калькулятор металлурга".
     * Выполняет инициализацию системы и запуск пользовательского интерфейса.
     *
     * <p><b>Последовательность выполнения:</b>
     * <ol>
     * <li>Инициализация подключения к базе данных через {@link DatabaseService#initialize()}</li>
     * <li>Создание и отображение окна аутентификации в потоке обработки событий Swing</li>
     * <li>Переход в режим ожидания пользовательского ввода</li>
     * </ol>
     * </p>
     *
     * <p><b>Особенности реализации:</b>
     * <ul>
     * <li>Использование {@code SwingUtilities.invokeLater()} гарантирует корректное
     *     создание GUI в потоке обработки событий Swing</li>
     * <li>Инициализация БД выполняется до создания GUI для обеспечения доступности данных</li>
     * <li>Приложение начинает работу с окна аутентификации для обеспечения безопасности</li>
     * </ul>
     * </p>
     *
     * @param args аргументы командной строки (не используются в текущей реализации)
     *
     * @throws Exception если произошла критическая ошибка при инициализации приложения.
     *         В реальной реализации следует обрабатывать исключения более детально.
     *
     * @see DatabaseService#initialize()
     * @see LoginFrame#LoginFrame()
     * @see SwingUtilities#invokeLater(Runnable)
     *
     * @example
     * <pre>
     * // Пример запуска приложения
     * public static void main(String[] args) {
     *     Main.main(args);
     * }
     * </pre>
     */
    public static void main(String[] args) {
        // Инициализация БД
        DatabaseService.initialize();

        // Запуск GUI
        SwingUtilities.invokeLater(() -> {
            new LoginFrame().setVisible(true);
        });
    }
}